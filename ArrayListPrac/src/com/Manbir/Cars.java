package com.Manbir;

public class Cars {
    private String color;
    private String make;
    private String engine;

    public Cars(String color, String make, String engine) {
        this.color = color;
        this.make = make;
        this.engine = engine;
    }

    public String getColor() {
        return color;
    }

    public String getMake() {
        return make;
    }

    public String getEngine() {
        return engine;
    }
}


